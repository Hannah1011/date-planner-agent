"""텍스트 전처리 유틸리티."""

import re


def strip_floor_info(address: str) -> str:
    """주소 끝부분의 층/호 정보를 제거해 API 검색에 적합한 형태로 반환한다.

    Examples:
        "서울 마포구 와우산로29길 47 1,2층" -> "서울 마포구 와우산로29길 47"
        "서울 마포구 성미산로29길 29-7 1층, 2층" -> "서울 마포구 성미산로29길 29-7"
        "서울 마포구 동교로12길 3 3층" -> "서울 마포구 동교로12길 3"

    Args:
        address: 원본 주소 문자열.

    Returns:
        층/호 정보를 제거한 주소 문자열.
    """
    # "1층", "1,2층", "1층, 2층", "B1층", "지하1층" 등 패턴 제거
    cleaned = re.sub(
        r"\s+(?:[Bb]?\d+(?:,\d+)*|지하\d*)층(?:[,\s]+(?:[Bb]?\d+(?:,\d+)*|지하\d*)층)*\s*$",
        "",
        address,
    )
    # "101호" 패턴 제거
    cleaned = re.sub(r"\s+\d+호\s*$", "", cleaned)
    return cleaned.strip()
